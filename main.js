if (form && feedback) {
  form.addEventListener("submit", (event) => {
    event.preventDefault();

    const formData = new FormData(form);
    const requiredFields = ["name", "email", "phone", "projectType", "message"];
    const missing = requiredFields.filter((field) => !String(formData.get(field) || "").trim());

    feedback.classList.remove("is-success", "is-error");

    if (missing.length > 0) {
      feedback.textContent =
        "Merci de completer les champs obligatoires avant de continuer.";
      feedback.classList.add("is-error");
      return;
    }

    const email = String(formData.get("email"));
    if (!email.includes("@") || !email.includes(".")) {
      feedback.textContent = "L'adresse email semble incomplete.";
      feedback.classList.add("is-error");
      return;
    }

    const projectType = String(formData.get("projectType"));
    const projectLabel = form.querySelector(
      `option[value="${CSS.escape(projectType)}"]`
    )?.textContent;

    feedback.textContent = `Mockup valide: demande "${projectLabel || projectType}" prete a etre connectee a un backend reel.`;
    feedback.classList.add("is-success");
  });
}

