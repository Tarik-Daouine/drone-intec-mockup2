function createMetricCard(metric) {
  const article = document.createElement("article");
  const strong = document.createElement("strong");
  const span = document.createElement("span");
  strong.textContent = metric.title;
  span.textContent = metric.text;
  article.append(strong, span);
  return article;
}

function createSectionCard(section) {
  const article = document.createElement("article");
  article.className = "service-section-card";

  const heading = document.createElement("h4");
  heading.textContent = section.title;
  article.appendChild(heading);

  (section.paragraphs || []).forEach((paragraph) => {
    const p = document.createElement("p");
    p.textContent = paragraph;
    article.appendChild(p);
  });

  if (section.bullets?.length) {
    const ul = document.createElement("ul");
    section.bullets.forEach((bullet) => {
      const li = document.createElement("li");
      li.textContent = bullet;
      ul.appendChild(li);
    });
    article.appendChild(ul);
  }

  return article;
}

function createGalleryItem(item) {
  const figure = document.createElement("figure");
  figure.className = "service-gallery-item";

  const img = document.createElement("img");
  img.src = item.src;
  img.alt = item.alt;

  const figcaption = document.createElement("figcaption");
  const span = document.createElement("span");
  const strong = document.createElement("strong");
  span.textContent = item.label;
  strong.textContent = item.title;
  figcaption.append(span, strong);

  figure.append(img, figcaption);
  return figure;
}

function renderService(key) {
  const service = serviceData[key];
  if (!service) {
    return;
  }

  panelKicker.textContent = service.kicker;
  panelTitle.textContent = service.title;
  panelDescription.textContent = service.description;

  serviceMainImage.src = service.mainImage;
  serviceMainImage.alt = service.mainAlt;
  serviceMainCaption.textContent = service.mainCaption;

  serviceSummaryLabel.textContent = service.summaryLabel;
  serviceSummaryText.textContent = service.summary;

  serviceChipList.innerHTML = "";
  service.chips.forEach((chip) => {
    const span = document.createElement("span");
    span.className = "service-chip";
    span.textContent = chip;
    serviceChipList.appendChild(span);
  });

  serviceSections.innerHTML = "";
  service.sections.forEach((section) => {
    serviceSections.appendChild(createSectionCard(section));
  });

  panelMetrics.innerHTML = "";
  service.metrics.forEach((metric) => {
    panelMetrics.appendChild(createMetricCard(metric));
  });

  serviceGalleryNote.textContent = service.galleryNote;
  serviceGallery.innerHTML = "";
  service.gallery.forEach((item) => {
    serviceGallery.appendChild(createGalleryItem(item));
  });

  serviceTabs.forEach((button) => {
    button.classList.toggle("is-active", button.dataset.service === key);
  });
}

serviceTabs.forEach((button) => {
  button.addEventListener("click", () => {
    renderService(button.dataset.service);
  });
});

function setupCustomSelect(select) {
  const options = Array.from(select.options);
  const selectableOptions = options.filter((option) => option.value);
  if (!selectableOptions.length) {
    return;
  }

  const shell = document.createElement("div");
  shell.className = "select-shell";

  const trigger = document.createElement("button");
  trigger.type = "button";
  trigger.className = "select-trigger";
  trigger.setAttribute("aria-haspopup", "listbox");
  trigger.setAttribute("aria-expanded", "false");

  const valueNode = document.createElement("span");
  valueNode.className = "select-value";
  trigger.appendChild(valueNode);

  const dropdown = document.createElement("div");
  dropdown.className = "select-dropdown";
  dropdown.setAttribute("role", "listbox");

  select.after(shell);
  shell.append(select, trigger, dropdown);
  select.classList.add("is-enhanced");

  selectableOptions.forEach((option) => {
    const item = document.createElement("button");
    item.type = "button";
    item.className = "select-option";
    item.dataset.value = option.value;
    item.setAttribute("role", "option");
    item.textContent = option.textContent;
    dropdown.appendChild(item);

    item.addEventListener("click", () => {
      select.value = option.value;
      select.dispatchEvent(new Event("change", { bubbles: true }));
      closeSelect();
      trigger.focus();
    });
  });

  function syncSelectState() {
    const selectedOption = select.options[select.selectedIndex];
    const selectedValue = select.value;

    valueNode.textContent = selectedOption ? selectedOption.textContent : "";
    trigger.classList.toggle("is-placeholder", !selectedValue);

    dropdown.querySelectorAll(".select-option").forEach((item) => {
      const isSelected = item.dataset.value === selectedValue;
      item.classList.toggle("is-selected", isSelected);
      item.setAttribute("aria-selected", String(isSelected));
    });
  }

  function closeSelect() {
    shell.classList.remove("is-open");
    trigger.setAttribute("aria-expanded", "false");
  }

  function openSelect() {
    document.querySelectorAll(".select-shell.is-open").forEach((node) => {
      if (node !== shell) {
        node.classList.remove("is-open");
        node.querySelector(".select-trigger")?.setAttribute("aria-expanded", "false");
      }
    });

    shell.classList.add("is-open");
    trigger.setAttribute("aria-expanded", "true");
  }

  trigger.addEventListener("click", () => {
    if (shell.classList.contains("is-open")) {
      closeSelect();
      return;
    }
    openSelect();
  });

  trigger.addEventListener("keydown", (event) => {
    if (event.key === "ArrowDown" || event.key === "Enter" || event.key === " ") {
      event.preventDefault();
      openSelect();
      dropdown.querySelector(".select-option.is-selected, .select-option")?.focus();
    }

    if (event.key === "Escape") {
      closeSelect();
    }
  });

  dropdown.addEventListener("keydown", (event) => {
    const items = Array.from(dropdown.querySelectorAll(".select-option"));
    const currentIndex = items.indexOf(document.activeElement);

    if (event.key === "Escape") {
      event.preventDefault();
      closeSelect();
      trigger.focus();
    }

    if (event.key === "ArrowDown" || event.key === "ArrowUp") {
      event.preventDefault();
      const direction = event.key === "ArrowDown" ? 1 : -1;
      const nextIndex =
        currentIndex >= 0
          ? (currentIndex + direction + items.length) % items.length
          : 0;
      items[nextIndex]?.focus();
    }
  });

  document.addEventListener("click", (event) => {
    if (!shell.contains(event.target)) {
      closeSelect();
    }
  });

  select.addEventListener("change", syncSelectState);
  syncSelectState();
}

